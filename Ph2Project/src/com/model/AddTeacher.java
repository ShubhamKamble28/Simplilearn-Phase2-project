package com.model;

public class AddTeacher {

	private int tid;
	private String title;
	private String tname;
	private String email;
	private String phoneno;
	private String subject;
	private String qualificataion;
	private String gender;
	
	
	public String getPhoneno() {
		return phoneno;
	}
	public void setPhoneno(String phoneno) {
		this.phoneno = phoneno;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getTsubject() {
		return subject;
	}
	public void setTsubject(String subject) {
		this.subject = subject;
	}
	public String getQualificataion() {
		return qualificataion;
	}
	public void setQualificataion(String qualificataion) {
		this.qualificataion = qualificataion;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	
}
